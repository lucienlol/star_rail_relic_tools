package com.example.starrail.vo;

public class RelicSetVO {

    String relicSetName;

    Integer relicSetType;

    Integer effectDemand;

    public String getRelicSetName() {
        return relicSetName;
    }

    public void setRelicSetName(String relicSetName) {
        this.relicSetName = relicSetName;
    }

    public Integer getRelicSetType() {
        return relicSetType;
    }

    public void setRelicSetType(Integer relicSetType) {
        this.relicSetType = relicSetType;
    }

    public Integer getEffectDemand() {
        return effectDemand;
    }

    public void setEffectDemand(Integer effectDemand) {
        this.effectDemand = effectDemand;
    }
}
